#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*a;
	int		i;

	i = 1;
	if (!lst)
		return (0);
	a = lst;
	while (a->next != NULL)
	{
		a = a->next;
		i++;
	}
	return (i);
}
