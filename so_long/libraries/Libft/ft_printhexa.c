#include "ft_printf.h"

void	ft_printhexa(unsigned long n)
{
	char	*str;

	str = "0123456789abcdef";
	if (n >= 16)
		ft_printhexa(n / 16);
	write(1, &str[(int)(n % 16)], 1);
}
