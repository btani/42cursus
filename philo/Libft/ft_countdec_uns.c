#include "libft.h"

int	ft_countdec_uns(unsigned int n)
{
	int					i;
	unsigned long int	a;

	a = n;
	if (a == 0)
		return (1);
	i = 0;
	while (a > 0)
	{
		i++;
		a = a / 10;
	}
	return (i);
}
